# Цель: Заглянуть в "паспорт" Химеры YOLOv5, чтобы узнать точные,
# буква в букву, имена всех классов объектов, которые она умеет распознавать.
# Это помогает избежать ошибок при фильтрации и подсчете.

# --- Акт 1: Подготовка Гримуара ---
# Призываем наш силовой гримуар PyTorch, так как YOLOv5 построен на нем.
import torch

# --- Акт 2: Ритуал "Допроса" Химеры ---

# `model = torch.hub.load(...)`: Мы призываем Химеру 'yolov5n'.
# Нам не важно, будет ли она на GPU, нам нужен только доступ к ее "памяти".
model = torch.hub.load('ultralytics/yolov5', 'yolov5n', pretrained=True)

# Сообщаем, какую информацию мы собираемся получить.
print("Имена, которые знает Химера:")
# `model.names`: Это обращение к "паспорту" Химеры, к его разделу `names`.
# Этот раздел представляет собой "магический словарь" (в Python - dict),
# где ключи - это номера классов (0, 1, 2...), а значения - их
# человекопонятные имена ('person', 'bicycle', 'car'...).
# `print()` автоматически красиво распечатает нам этот словарь.
print(model.names)