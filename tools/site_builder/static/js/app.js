      const promptSpan = document.createElement("span");
      // –ú—ã –ø—Ä–∏–¥–∞–µ–º —Å–∏–≥–∏–ª—É –µ–≥–æ –º–∞–≥–∏—á–µ—Å–∫–∏–π –æ–±–ª–∏–∫ ('prompt').
      promptSpan.className = "prompt";
      // –ú—ã –≤–ø–∏—Å—ã–≤–∞–µ–º –≤ –Ω–µ–≥–æ —Å–ª–æ–≤–∞ "mage@codex:~$".
      promptSpan.textContent = "mage@codex:~$";

