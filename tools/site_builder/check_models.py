"""–í–µ–ª–∏–∫–∏–π –ì—Ä–∏–º—É–∞—Ä: –û–±–æ–∑—Ä–µ–≤–∞—Ç–µ–ª—å –û—Ä–∞–∫—É–ª–æ–≤ (check_models.py)

–≠—Ç–æ—Ç –ø–µ—Ä–≥–∞–º–µ–Ω—Ç —Å–æ–¥–µ—Ä–∂–∏—Ç –ø—Ä–æ—Å—Ç–æ–µ, –Ω–æ –≤–∞–∂–Ω–æ–µ –∑–∞–∫–ª–∏–Ω–∞–Ω–∏–µ-—Ä–∞–∑–≤–µ–¥—á–∏–∫. –ï–≥–æ –µ–¥–∏–Ω—Å—Ç–≤–µ–Ω–Ω–∞—è —Ü–µ–ª—å ‚Äî
–æ—Ç–ø—Ä–∞–≤–∏—Ç—å "–¥—É—Ö–∞-–ø–æ—Å–ª–∞–Ω–Ω–∏–∫–∞" –≤ –í–µ–ª–∏–∫–∏–π –ü–∞–Ω—Ç–µ–æ–Ω OpenRouter –∏ –∑–∞–ø—Ä–æ—Å–∏—Ç—å —É –Ω–µ–≥–æ –ø–æ–ª–Ω—ã–π
—Å–ø–∏—Å–æ–∫ –≤—Å–µ—Ö –¥–æ—Å—Ç—É–ø–Ω—ã—Ö –û—Ä–∞–∫—É–ª–æ–≤ (–º–æ–¥–µ–ª–µ–π).

–†–∏—Ç—É–∞–ª –æ—Å–æ–±–µ–Ω–Ω–æ —Ü–µ–Ω–µ–Ω —Ç–µ–º, —á—Ç–æ –æ–Ω —Ñ–∏–ª—å—Ç—Ä—É–µ—Ç —ç—Ç–æ—Ç —Å–ø–∏—Å–æ–∫, –≤—ã–¥–µ–ª—è—è —Ç–æ–ª—å–∫–æ —Ç–µ—Ö "–¥—É—Ö–æ–≤",
–∫–æ—Ç–æ—Ä—ã–µ —Å–ª—É–∂–∞—Ç –±–µ–∑–≤–æ–∑–º–µ–∑–¥–Ω–æ (–æ—Ç–º–µ—á–µ–Ω—ã —Ä—É–Ω–æ–π ':free'). –≠—Ç–æ –ø–æ–∑–≤–æ–ª—è–µ—Ç –ú–∞–≥—É-–¢–µ—Ö–Ω–æ–º–∞–Ω—Ç—É
–≤—Å–µ–≥–¥–∞ –±—ã—Ç—å –≤ –∫—É—Ä—Å–µ –¥–æ—Å—Ç—É–ø–Ω—ã—Ö –±–µ—Å–ø–ª–∞—Ç–Ω—ã—Ö –∏–Ω—Å—Ç—Ä—É–º–µ–Ω—Ç–æ–≤ –¥–ª—è —Å–≤–æ–∏—Ö —ç–∫—Å–ø–µ—Ä–∏–º–µ–Ω—Ç–æ–≤, –Ω–µ —Ç—Ä–∞—Ç—è
–¥—Ä–∞–≥–æ—Ü–µ–Ω–Ω—É—é –º–∞–Ω—É.
"""

# --- –ê–∫—Ç 1: –ü—Ä–∏–∑—ã–≤ –ì—Ä–∏–º—É–∞—Ä–∞ ---
# –ú—ã –ø—Ä–∏–∑—ã–≤–∞–µ–º –≥—Ä–∏–º—É–∞—Ä 'requests' ‚Äî –Ω–∞—à–µ–≥–æ "–¥—É—Ö–∞-–ø–æ—Å–ª–∞–Ω–Ω–∏–∫–∞", —É–º–µ—é—â–µ–≥–æ –ª–µ—Ç–∞—Ç—å –ø–æ —Å–µ—Ç–∏.
import requests


# --- –ê–∫—Ç 2: –ß–µ—Ä—Ç–µ–∂ –ì–ª–∞–≤–Ω–æ–≥–æ –†–∏—Ç—É–∞–ª–∞ ---
# –ú—ã –æ–ø–∏—Å—ã–≤–∞–µ–º —á–µ—Ä—Ç–µ–∂ –Ω–∞—à–µ–≥–æ —Ä–∏—Ç—É–∞–ª–∞-—Ä–∞–∑–≤–µ–¥—á–∏–∫–∞.
def get_free_models():
    # –ú—ã –∑–∞–ø–∏—Å—ã–≤–∞–µ–º —Ç–æ—á–Ω—ã–π –∞–¥—Ä–µ—Å "–ú–∞–≥–∏—á–µ—Å–∫–æ–≥–æ –ü–æ—Ä—Ç–∞–ª–∞",
    # –≥–¥–µ –Ω–∞—Ö–æ–¥–∏—Ç—Å—è —Å–ø–∏—Å–æ–∫ –≤—Å–µ—Ö –û—Ä–∞–∫—É–ª–æ–≤.
    url = "https://openrouter.ai/api/v1/models"
    # –ú—ã –æ—Ç–ø—Ä–∞–≤–ª—è–µ–º "–¥—É—Ö–∞-–ø–æ—Å–ª–∞–Ω–Ω–∏–∫–∞" –ø–æ —ç—Ç–æ–º—É –∞–¥—Ä–µ—Å—É
    # —Å –ø—Ä–æ—Å—Ç—ã–º –∑–∞–ø—Ä–æ—Å–æ–º "–ø–æ–∫–∞–∂–∏, —á—Ç–æ —É —Ç–µ–±—è –µ—Å—Ç—å".
    response = requests.get(url)

    # "–°—Ç—Ä–∞–∂-–ü—Ä–æ–≤–µ—Ä–∫–∞": –µ—Å–ª–∏ "–¥—É—Ö-–ø–æ—Å–ª–∞–Ω–Ω–∏–∫" –≤–µ—Ä–Ω—É–ª—Å—è
    # —Å —Ö–æ—Ä–æ—à–∏–º–∏ –≤–µ—Å—Ç—è–º–∏ (—Å—Ç–∞—Ç—É—Å 200 - "OK")...
    if response.status_code == 200:
        # ...–º—ã –∏–∑–≤–ª–µ–∫–∞–µ–º –∏–∑ –µ–≥–æ –ø–æ—Å–ª–∞–Ω–∏—è "—Å–µ—Ä–¥—Ü–µ–≤–∏–Ω—É" ‚Äî —Å–ø–∏—Å–æ–∫ –¥–∞–Ω–Ω—ã—Ö –æ –º–æ–¥–µ–ª—è—Ö.
        data = response.json()["data"]
        # –ú—ã –æ–≥–ª–∞—à–∞–µ–º –Ω–∞ –∫—Ä–∏—Å—Ç–∞–ª–ª (–∫–æ–Ω—Å–æ–ª—å), —Å–∫–æ–ª—å–∫–æ –≤—Å–µ–≥–æ –û—Ä–∞–∫—É–ª–æ–≤ –±—ã–ª–æ –Ω–∞–π–¥–µ–Ω–æ.
        print(f"–í—Å–µ–≥–æ –º–æ–¥–µ–ª–µ–π –¥–æ—Å—Ç—É–ø–Ω–æ: {len(data)}")
        # –ú—ã —Ä–∏—Å—É–µ–º —Ä–∞–∑–¥–µ–ª–∏—Ç–µ–ª—å–Ω—É—é –ª–∏–Ω–∏—é –¥–ª—è –∫—Ä–∞—Å–æ—Ç—ã.
        print("-" * 40)
        # –ú—ã –æ–±—ä—è–≤–ª—è–µ–º, —á—Ç–æ —Å–µ–π—á–∞—Å –ø–æ–∫–∞–∂–µ–º —Ç–æ–ª—å–∫–æ "–±–µ–∑–≤–æ–∑–º–µ–∑–¥–Ω—ã—Ö" –¥—É—Ö–æ–≤.
        print("üíé –ë–ï–°–ü–õ–ê–¢–ù–´–ï –ú–û–î–ï–õ–ò ( :free ):")
        # –°–Ω–æ–≤–∞ —Ä–∏—Å—É–µ–º –ª–∏–Ω–∏—é.
        print("-" * 40)

        # –ú—ã —Å–æ–∑–¥–∞–µ–º –ø—É—Å—Ç–æ–π "—Å–≤–∏—Ç–æ–∫" –¥–ª—è —Ö—Ä–∞–Ω–µ–Ω–∏—è –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏–∏ –æ –±–µ—Å–ø–ª–∞—Ç–Ω—ã—Ö –º–æ–¥–µ–ª—è—Ö.
        free_models = []
        # –ú—ã –Ω–∞—á–∏–Ω–∞–µ–º –≤–µ–ª–∏–∫–∏–π —Ü–∏–∫–ª, –ø–µ—Ä–µ–±–∏—Ä–∞—è –∫–∞–∂–¥–æ–≥–æ –û—Ä–∞–∫—É–ª–∞ –∏–∑ –ø–æ–ª—É—á–µ–Ω–Ω–æ–≥–æ —Å–ø–∏—Å–∫–∞.
        for model in data:
            # –ú—ã –ø—Ä–æ–≤–µ—Ä—è–µ–º, –µ—Å—Ç—å –ª–∏ –≤ "–∏–¥–µ–Ω—Ç–∏—Ñ–∏–∫–∞—Ü–∏–æ–Ω–Ω–æ–π —Ä—É–Ω–µ"
            # (ID) –û—Ä–∞–∫—É–ª–∞ –∑–Ω–∞–∫ ":free".
            if ":free" in model["id"]:
                # –ï—Å–ª–∏ –∑–Ω–∞–∫ –µ—Å—Ç—å, –º—ã –¥–æ–±–∞–≤–ª—è–µ–º —ç—Ç–æ–≥–æ –û—Ä–∞–∫—É–ª–∞ –≤ –Ω–∞—à "—Å–≤–∏—Ç–æ–∫".
                free_models.append(model)
                # –ú—ã –∏–∑–≤–ª–µ–∫–∞–µ–º —Ä–∞–∑–º–µ—Ä –µ–≥–æ "–ø–∞–º—è—Ç–∏" (context_length).
                # –ï—Å–ª–∏ –æ–Ω –Ω–µ —É–∫–∞–∑–∞–Ω, –ø–∏—à–µ–º "Unknown".
                context = model.get("context_length", "Unknown")
                # –ú—ã –≤—ã–≤–æ–¥–∏–º –Ω–∞ –∫—Ä–∏—Å—Ç–∞–ª–ª –µ–≥–æ –∏–º—è.
                print(f"‚Ä¢ ID: {model['id']}")
                # –ò —Ä–∞–∑–º–µ—Ä –µ–≥–æ "–ø–∞–º—è—Ç–∏".
                print(f"  Context: {context}")
                # –†–∏—Å—É–µ–º –º–∞–ª–µ–Ω—å–∫—É—é —Ä–∞–∑–¥–µ–ª–∏—Ç–µ–ª—å–Ω—É—é –ª–∏–Ω–∏—é.
                print("-" * 20)

        # –ú—ã –≤–æ–∑–≤—Ä–∞—â–∞–µ–º —Å–æ–±—Ä–∞–Ω–Ω—ã–π "—Å–≤–∏—Ç–æ–∫" —Å –±–µ—Å–ø–ª–∞—Ç–Ω—ã–º–∏ –û—Ä–∞–∫—É–ª–∞–º–∏.
        return free_models
    # –ï—Å–ª–∏ –∂–µ "–¥—É—Ö-–ø–æ—Å–ª–∞–Ω–Ω–∏–∫" –≤–µ—Ä–Ω—É–ª—Å—è —Å –ø–ª–æ—Ö–∏–º–∏ –≤–µ—Å—Ç—è–º–∏...
    else:
        # ...–º—ã —Å–æ–æ–±—â–∞–µ–º –ú–∞–≥—É –æ–± –æ—à–∏–±–∫–µ...
        print("–û—à–∏–±–∫–∞ –ø–æ–¥–∫–ª—é—á–µ–Ω–∏—è –∫ OpenRouter")
        # ...–∏ –≤–æ–∑–≤—Ä–∞—â–∞–µ–º –ø—É—Å—Ç–æ–π "—Å–≤–∏—Ç–æ–∫".
        return []


# --- –ê–∫—Ç 3: "–°–≤—è—â–µ–Ω–Ω–æ–µ –ù–∞—á–∞–ª–æ" ---
# –≠—Ç–æ—Ç –±–ª–æ–∫ –≤—ã–ø–æ–ª–Ω—è–µ—Ç—Å—è, —Ç–æ–ª—å–∫–æ –∫–æ–≥–¥–∞ –ø–µ—Ä–≥–∞–º–µ–Ω—Ç –∑–∞–ø—É—Å–∫–∞–µ—Ç—Å—è –Ω–∞–ø—Ä—è–º—É—é.
if __name__ == "__main__":
    # –ú—ã –∑–∞–ø—É—Å–∫–∞–µ–º –Ω–∞—à –≥–ª–∞–≤–Ω—ã–π —Ä–∏—Ç—É–∞–ª-—Ä–∞–∑–≤–µ–¥—á–∏–∫.
    get_free_models()
