# Мы призываем гримуар 'os', который дает нам власть над операционной системой.
# Однако, мы нигде в этом пергаменте не используем его силу.
# Это — "мнимая мудрость", первый изъян. Страж `flake8` должен обнаружить
# этот неиспользуемый импорт и сообщить об ошибке `F401`.
# Этот гримуар призван, но не используется. Flake8 должен это заметить
# (ошибка F401).
import os  # noqa: F401


# Мы начинаем определение нашего "несовершенного заклинания".
# Уже в этой строке скрыто множество стилистических изъянов:
# `( a,b )` — пробелы внутри скобок. Страж `flake8`
#  должен заметить их и сообщить об ошибках `E201` и `E202`.
# `def bad_spell` — здесь все по канону.
# Здесь лишние пробелы. Flake8 должен это заметить (ошибки E201, E202, E225).
def bad_spell(a, b):
    # Главное магическое действие: заклинание `return` "материализует" результат.
    # `a+b` — здесь отсутствует пробел вокруг оператора `+`. Страж должен заметить это
    # и сообщить об ошибке `E225`.
    return a + b


