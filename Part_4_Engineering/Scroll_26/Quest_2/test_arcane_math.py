# Мы призываем то самое заклинание, которое собираемся подвергнуть испытанию.
from arcane_math import add_two_runes


# Древний канон гласит: Имя любого ритуала-испытания должно начинаться с
# 'test_', чтобы "Всевидящее Око" (Pytest) могло его найти.
def test_add_two_runes_positive():
    # Это "официальное описание" ритуала-испытания, которое объясняет, какую
    # именно истину мы проверяем.
    """
    Этот ритуал проверяет, правильно ли работает заклинание сложения с
    положительными числами.
    """
    # `assert` — это могущественное заклинание "Я УТВЕРЖДАЮ, ЧТО...".
    # Если утверждение оказывается истинным, ритуал считается успешным.
    # Если ложным — ритуал провален, и магия считается несовершенной.
    # Мы утверждаем, что результат сложения рун '2' и '3' должен быть в
    # точности равен '5'.
    assert add_two_runes(2, 3) == 5


# Мы начинаем второй ритуал-испытание, также названный по канону.
def test_add_two_runes_negative_and_zero():
    # Описание этого, более сложного, испытания.
    """Этот ритуал проверяет работу с отрицательными числами и нулем."""
    # Первое утверждение: сложение рун '-1' и '1' должно в точности дать '0'.
    assert add_two_runes(-1, 1) == 0
    # Второе утверждение в этом же ритуале: сложение двух отрицательных рун
    # '-5' и '-5' должно дать '-10'.
    assert add_two_runes(-5, -5) == -10
