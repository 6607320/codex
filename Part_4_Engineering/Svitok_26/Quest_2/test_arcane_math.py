# Призываем заклинание, которое будем проверять.
from arcane_math import add_two_runes


# Имя ритуала-теста должно начинаться с 'test_'.
def test_add_two_runes_positive():
    """Этот ритуал проверяет, правильно ли работает заклинание сложения с положительными числами."""
    # 'assert' - это заклинание "Я утверждаю, что...".
    # Если утверждение истинно, тест пройден. Если ложно - провален.
    assert add_two_runes(2, 3) == 5


def test_add_two_runes_negative_and_zero():
    """Этот ритуал проверяет работу с отрицательными числами и нулем."""
    assert add_two_runes(-1, 1) == 0
    assert add_two_runes(-5, -5) == -10
