# Эта строка — не просто комментарий, а священная руна "Шебанг" — "Печать Интерпретатора".
# Она произносится в самом начале пергамента и приказывает "Великому Духу-Исполнителю"
# операционной системы использовать для чтения этого свитка именно "Язык Командной Строки Баш" (`/bin/bash`).
# Без этой руны Дух мог бы растеряться, не зная, как толковать наши приказы.
#!/bin/bash

# Это — Главное Заклинание Жизни, которое "оживляет" наш Амулет. Мы приказываем "Духу-Гонцу" (`uvicorn`):
# 1. Найди в пергаменте `main.py` артефакт по имени `app` (`main:app`).
# 2. С помощью "Руны Всеслышания" (`--host 0.0.0.0`) слушай весь мир, а не только себя (это критически важно для работы в контейнерах).
# 3. Открой "Магический Портал" (`--port`) на номере, который будет указан в "секретной руне окружения" по имени `${PORT}`.
#    Это заклинание мудрости и гибкости, позволяющее нашему артефакту работать в любом магическом царстве (облачной платформе).
uvicorn main:app --host 0.0.0.0 --port ${PORT}