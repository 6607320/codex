# Эта первая руна - магический знак принадлежности. Она объявляет: "Этот свиток написан на древнем языке Bash, и только дух-толкователь Bash может прочесть его и исполнить волю Мастера".
#!/bin/bash

# Это и есть само Заклинание Пробуждения, главная строка ритуала.
# Мы призываем Голема-Стража 'uvicorn' и отдаем ему три нерушимых приказа:
# 1. "Найди в главном пергаменте ('main') магический портал по имени 'app' и оживи его своей силой!"
# 2. "Стой на страже на всех фронтах ('--host 0.0.0.0'), слушай не только внутренний голос, но и зов извне, со всей магической сети".
# 3. "Открой тот портал, чей номер я прошептал тебе при сотворении ('--port ${PORT}'). Через него мы будем общаться".
uvicorn main:app --host 0.0.0.0 --port ${PORT}